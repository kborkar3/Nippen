package com.projectwork.bean;

import java.util.Calendar;
import java.util.List;

public class ParameterBean
{
    private List parameterList;
    private String parameterIndex;
    private String parameterName;
    private float parameterValue;
    String parameterRYGB;
    String parameterType;
    String parameterFormat;
    private Calendar dateTimeStamp;
    
    public List getParameterList()
    {
        return parameterList;
    }

    public void setParameterList(List parameterList)
    {
        this.parameterList = parameterList;
    }

    public String getParameterName()
    {
        return parameterName;
    }

    public void setParameterName(String parameterName)
    {
        this.parameterName = parameterName;
    }

    public float getParameterValue()
    {
        return parameterValue;
    }

    public void setParameterValue(float parameterValue)
    {
        this.parameterValue = parameterValue;
    }

    public String getParameterRYGB()
    {
        return parameterRYGB;
    }

    public void setParameterRYGB(String parameterRYGB)
    {
        this.parameterRYGB = parameterRYGB;
    }

    public String getParameterType()
    {
        return parameterType;
    }

    public void setParameterType(String parameterType)
    {
        this.parameterType = parameterType;
    }

    public String getParameterFormat()
    {
        return parameterFormat;
    }

    public void setParameterFormat(String parameterFormat)
    {
        this.parameterFormat = parameterFormat;
    }

    public String getParameterIndex()
    {
        return parameterIndex;
    }

    public void setParameterIndex(String parameterIndex)
    {
        this.parameterIndex = parameterIndex;
    }
    public Calendar getDateTimeStamp()
    {
        return dateTimeStamp;
    }
    public void setDateTimeStamp(Calendar dateTimeStamp)
    {
        this.dateTimeStamp = dateTimeStamp;
    }
}
